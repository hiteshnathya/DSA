package Day_18.sdp;

public class Volt {
    // Instance variable to store the voltage value
    private int volts;

    // Constructor to initialize the voltage value
    public Volt(int volts) {
        this.volts = volts;
    }

    // Getter method to retrieve the voltage value
    public int getVolts() {
        return volts;
    }

    // Setter method to set the voltage value
    public void setVolts(int volts) {
        this.volts = volts;
    }
}